<?php

namespace App\Modules\Recruitment\Requests;

use App\Modules\Recruitment\Abstracts\BaseRecruitmentRequest;
use App\Modules\Recruitment\Enums\RecruitmentStatus;
use App\Modules\Recruitment\Models\Recruitment;
use App\Modules\Recruitment\Rules\RecruitmentStatusTransitionsFromRule;
use Illuminate\Support\Carbon;
use Illuminate\Support\Facades\Gate;
use Illuminate\Validation\Rule;

class RecruitmentPublishRequest extends BaseRecruitmentRequest
{
    public Recruitment $recruitment;

    public function rules(): array
    {
        return [
            /**
             * Initial status (generated by server)
             * @ignoreParam
             */
            "status" => [
                "required",
                Rule::enum(RecruitmentStatus::class)->only(RecruitmentStatus::PUBLISHED),
                new RecruitmentStatusTransitionsFromRule($this->recruitment->status),
            ],
            /**
             * Publish timestamp (generated by server)
             * @ignoreParam
             */
            "actual_published_at" => ["required", "date"],
        ];
    }

    public function authorize(): bool
    {
        Gate::authorize("publish", $this->recruitment);
        return true;
    }

    public function prepareForValidation(): void
    {
        parent::prepareForValidation();

        $this->recruitment = Recruitment::findOrFail($this->route("id"));

        $this->merge([
            "status" => RecruitmentStatus::PUBLISHED->value,
            "published_at" => Carbon::now(),
        ]);
    }
}
